    private void txfcodigoKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txfcodigoKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!"".equals(txfcodigo.getText())) {
                String cod = txfcodigo.getText();
                pro = proDao.BuscarPro(cod);
                if (pro.getNombre() != null) {
                    txfcodigo.setText("" + pro.getId());
                    txfdescripcion.setText("" + pro.getNombre());
                    txfprecio1.setText("" + pro.getPrecio());
                    txfstock.setText("" + pro.getStock());
                    txfcantidad1.requestFocus();
                } else {
                    LimparVenta();
                    txfcodigo.requestFocus();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Ingrese el codigo del productos");
                txfcodigo.requestFocus();
            }
        }
    }//GEN-LAST:event_txfcodigoKeyPressed


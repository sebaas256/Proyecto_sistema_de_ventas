    private void LimparVenta() {
        txfcodigo.setText("");
        txfdescripcion.setText("");
        txfcantidad1.setText("");
        txfstock.setText("");
        txfprecio1.setText("");
    }
    private void TotalPagar() {
        Totalpagar = 0.00;
        int numFila = jTable1.getRowCount();
        for (int i = 0; i < numFila; i++) {
            double cal = Double.parseDouble(String.valueOf(jTable1.getModel().getValueAt(i, 3)));
            Totalpagar = Totalpagar + cal;
        }
        LabelTotal.setText(String.format("$"+"%.2f", Totalpagar));
    }
    private void RegistrarVenta() {
        double monto = Totalpagar;
        int dui = Integer.parseInt(txfdui.getText());
        v.setTotal(monto);
        v.setdui(dui);
        vdb.RegistrarVenta(v);
    }
    private void Cambio(){
        double pagoCon = Double.parseDouble(txfpago.getText());
        double total_devolver = pagoCon-Totalpagar;
        txfcambio.setText(String.valueOf("$"+total_devolver));
    }
    
    private void ActualizarStock() {
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String cod = (jTable1.getValueAt(i, 0).toString());
            int cant = Integer.parseInt(jTable1.getValueAt(i, 2).toString());
            pro = proDao.BuscarPro(cod);
            int StockActual = pro.getStock() - cant;
            vdb.ActualizarStock(StockActual, cod);
        }
    }

    
    

